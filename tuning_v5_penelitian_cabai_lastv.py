# 2. Mencari K optimal (K = 2, 3, 4, 5) menggunakan Silhouette dan Inertia (Elbow)
K_range = [2, 3, 4, 5]
inertia_values = []
silhouette_scores = []

print("=== Evaluasi Jumlah Cluster (K) ===")
best_k = 2
best_score = -1

for k in K_range:
    # random_state=42 agar hasil konsisten setiap kali di-run
    # n_init=10 untuk menghindari warning scikit-learn versi terbaru
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    kmeans.fit(X)

    # Simpan nilai Inertia (untuk Metode Elbow)
    inertia_values.append(kmeans.inertia_)

    # Hitung dan simpan Silhouette Score
    score = silhouette_score(X, kmeans.labels_)
    silhouette_scores.append(score)

    print(f"K = {k} | Inertia = {kmeans.inertia_:.4f} | Silhouette Score = {score:.4f}")

    # Update K terbaik berdasarkan Silhouette Score tertinggi

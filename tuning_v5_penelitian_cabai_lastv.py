import pandas as pd
from arch import arch_model
import warnings

# Mengabaikan warning konvergensi agar output terminal tetap bersih
warnings.filterwarnings('ignore')

# 1. Memuat dataset yang sudah bersih
df = pd.read_csv('dataset_garch_cabai_bersih.csv')

# 2. Mengalikan Return dengan 100
df['Return_100'] = df['Return'] * 100

hasil_volatilitas = []

provinsi_unik = df['provinsi'].unique()

print(f"Mulai melatih model GARCH(1,1) untuk {len(provinsi_unik)} Provinsi...")
print("Proses sedang berjalan, mohon tunggu sebentar...\n")

for prov in provinsi_unik:
    df_prov = df[df['provinsi'] == prov]
    volatilitas_komoditas = []

    for komoditas in df_prov['name'].unique():
        returns = df_prov[df_prov['name'] == komoditas]['Return_100'].dropna()

        if len(returns) < 10:
            continue

        try:
            model = arch_model(returns, mean='Constant', vol='GARCH', p=1, q=1)
            res = model.fit(disp='off')
            rata_rata_vol = res.conditional_volatility.mean()
            volatilitas_komoditas.append(rata_rata_vol)
        except Exception as e:
            pass

    if volatilitas_komoditas:
        vol_provinsi = sum(volatilitas_komoditas) / len(volatilitas_komoditas)
        hasil_volatilitas.append({
            'Provinsi': prov,
            'Nilai_Volatilitas': vol_provinsi
        })

print(" Proses fitting GARCH selesai!\n")

# 2. Membuat DataFrame dan menampilkan hasil
if len(hasil_volatilitas) > 0:
    df_hasil = pd.DataFrame(hasil_volatilitas)
    df_hasil = df_hasil.sort_values(by='Nilai_Volatilitas', ascending=False).reset_index(drop=True)

    print(f"Total Provinsi yang berhasil diekstrak volatilitasnya: {len(df_hasil)}")
    print("-" * 50)
    print(df_hasil.to_string())

    df_hasil.to_csv('hasil_volatilitas_garch_provinsi.csv', index=False)
    print("\n Data telah disimpan sebagai 'hasil_volatilitas_garch_provinsi.csv'")
else:
    print(" Gagal: Tidak ada provinsi yang berhasil dimodelkan. Cek kembali isi data 'Return_100'.")

